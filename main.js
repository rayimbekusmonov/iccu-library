/* ─────────────────────────────────────────────
   Lang management
───────────────────────────────────────────── */
let currentLang = "uz";

const langMeta = {
  uz: { flag:"🇺🇿", label:"O'zbek" },
  en: { flag:"🇬🇧", label:"English" },
  ru: { flag:"🇷🇺", label:"Русский" },
  tr: { flag:"🇹🇷", label:"Türkçe" }
};

function setLang(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  const m = langMeta[lang];
  const trigger = document.getElementById("lang-trigger");
  trigger.querySelector(".lt-flag").textContent  = m.flag;
  trigger.querySelector(".lt-label").textContent = m.label;
  closeLangMenu();
  applyTexts();
}

function applyTexts() {
  const dict = i18n[currentLang] || i18n.uz;
  document.querySelectorAll("[data-t]").forEach(el => {
    const key = el.getAttribute("data-t");
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
}

function toggleLangMenu() {
  const menu    = document.getElementById("lang-menu");
  const trigger = document.getElementById("lang-trigger");
  const isOpen  = menu.classList.toggle("open");
  trigger.classList.toggle("open", isOpen);
}

function closeLangMenu() {
  document.getElementById("lang-menu").classList.remove("open");
  document.getElementById("lang-trigger").classList.remove("open");
}

document.addEventListener("click", e => {
  if (!e.target.closest(".lang-wrap")) closeLangMenu();
});

/* ─────────────────────────────────────────────
   Page navigation
───────────────────────────────────────────── */
function showPage(name) {
  document.querySelectorAll(".page").forEach(p => p.classList.remove("active"));
  document.querySelectorAll(".nav-link").forEach(b => b.classList.remove("active"));
  const page = document.getElementById(`page-${name}`);
  const nav  = document.getElementById(`nav-${name}`);
  if (page) page.classList.add("active");
  if (nav)  nav.classList.add("active");
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function scrollToSection(id) {
  const el = document.getElementById(id);
  if (el) el.scrollIntoView({ behavior: "smooth", block: "start" });
}

/* ─────────────────────────────────────────────
   News detail
───────────────────────────────────────────── */
function openNews(index) {
  const dict = i18n[currentLang] || i18n.uz;
  const n = index + 1;
  const dates = {
    uz:["2 iyun 2026","28 may 2026","20 may 2026"],
    en:["2 June 2026","28 May 2026","20 May 2026"],
    ru:["2 июня 2026","28 мая 2026","20 мая 2026"],
    tr:["2 Haziran 2026","28 Mayıs 2026","20 Mayıs 2026"]
  };
  document.getElementById("detail-tag").textContent     = dict[`newsTag${n}`]      || "";
  document.getElementById("detail-title").textContent   = dict[`newsHeadline${n}`] || "";
  document.getElementById("detail-heading").textContent = dict[`newsHeadline${n}`] || "";
  document.getElementById("detail-body").textContent    = dict[`newsBody${n}`]     || "";
  document.getElementById("detail-extra").textContent   = dict[`newsExtra${n}`]    || "";
  document.getElementById("detail-date").textContent    = (dates[currentLang] || dates.en)[index];
  showPage("news-detail");
}

/* ─────────────────────────────────────────────
   Video modal
───────────────────────────────────────────── */
function openVideo(index) {
  const dict = i18n[currentLang] || i18n.uz;
  const n = index + 1;
  document.getElementById("video-modal-title").textContent = dict[`video${n}Title`] || "";
  document.getElementById("video-modal-meta").textContent  = dict[`video${n}Meta`]  || "";
  document.getElementById("video-modal").classList.add("open");
  document.body.classList.add("modal-open");
}

function closeVideo() {
  document.getElementById("video-modal").classList.remove("open");
  document.body.classList.remove("modal-open");
}

/* ─────────────────────────────────────────────
   FAQ
───────────────────────────────────────────── */
function toggleFaq(id, button) {
  const answer    = document.getElementById(id);
  const indicator = button.querySelector("span:last-child");
  const isOpen    = answer.classList.contains("open");
  document.querySelectorAll(".faq-answer").forEach(a => a.classList.remove("open"));
  document.querySelectorAll(".faq-question span:last-child").forEach(s => s.textContent = "+");
  if (!isOpen) { answer.classList.add("open"); indicator.textContent = "−"; }
}

